#include <stdio.h>

int main(void){
	int a;
	char c;
	char s[10] = "123456789";

	a = 0;
	c = 'c';
	while (a < 10){
		printf("%d : %c : %c\n", a, c, s[a]);
		a++;
	}
	return (0);
}
